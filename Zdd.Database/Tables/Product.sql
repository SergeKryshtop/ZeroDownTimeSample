	[Class] [nchar](2) NULL,
	[Style] [nchar](2) NULL,
	[ProductSubCategoryId] [int] NULL,
	[ProductModelId] [int] NULL,
	[SellStartDate] [datetime] NOT NULL,
	[SellEndDate] [datetime] NULL,
	[DiscontinuedDate] [datetime] NULL,
